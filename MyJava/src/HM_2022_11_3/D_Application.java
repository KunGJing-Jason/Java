package HM_2022_11_3;

import static java.lang.Math.*;

public class D_Application {
    public static void main(String[] args){
        System.out.println("-12的绝对值为 ："+abs(-12.00));
        System.out.println("98.89 和 6498.00的最大值为 ："+ max(98.89, 6498.00));
        System.out.println("98.89 和 6498.00的最小值为 ："+ min(98.89, 6498.00));
        System.out.println("0-1之间的随机数 ：" + random());
        System.out.println("2的10次方为 ：" + pow(2,10));
        System.out.println("36的算数平方根为 ：" + sqrt(36));
        System.out.println("8的对数为 ：" + log(8));
        System.out.println("45的正弦值为 ：" + sin(45));
        System.out.println("45的反正弦值为 ：" + asin(45));
        System.out.println("-12.56四舍五入为 ："+ round(-12.56));
        System.out.println("12.56四舍五入为 ："+ round(12.56));
    }
}


